let a=6;
let b="6";
console.log(a==b); // Equal Value.
console.log(a!=b); // Not Equal Value.
console.log(a===b); // Equal Value and Equal type i.e. str or int, etc..
console.log(a!==b); // Not Equal Value or Not Equal type i.e. str or int, etc..
console.log(a>=b); // a is greater or equal to b.
console.log(a<=b); // b is greater or equal to a.