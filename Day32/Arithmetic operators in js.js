console.log(" Arithmetic Operators in Javascript");
let a=10;
let b=4;
console.log("a + b=", a+b);
console.log("a - b=", a-b);
console.log("a / b=", a/b);
console.log("a ** b=", a**b);
console.log("a % b=", a%b);
console.log("a++ =", a++); // this prints a first, then increments the value of a.
console.log("++a =", ++a); // this increments first, then prints the value of a.
console.log("a-- =", a--); // this prints a first, then decrements the value of a.
console.log("--a =", --a); // this decrements first, then prints the value of a.
